package de.net25.resources.statics;


public class StaticResources {

  private String dataSource;
	private String languages;
	private String basePath;
	private String imagePath;
	private String documentPath;
	private String staticBase;
	private String dynamicBase;
	private String pageCacheSize;
	private String docCacheSize;
	private String imgCacheSize;
	private String mailServer;
	private String mailSender;
	private String mailAddress;

  public String getDataSource() {
    return dataSource;
  }

  public void setDataSource(String dataSource) {
    this.dataSource = dataSource;
  }

  public String getLanguages() {
    return languages;
  }

  public void setLanguages(String languages) {
    this.languages = languages;
  }

  public String getBasePath() {
    return basePath;
  }

  public void setBasePath(String basePath) {
    this.basePath = basePath;
  }

  public String getImagePath() {
    return imagePath;
  }

  public void setImagePath(String imagePath) {
    this.imagePath = imagePath;
  }

  public String getDocumentPath() {
    return documentPath;
  }

  public void setDocumentPath(String documentPath) {
    this.documentPath = documentPath;
  }

  public String getStaticBase() {
    return staticBase;
  }

  public void setStaticBase(String staticBase) {
    this.staticBase = staticBase;
  }

  public String getDynamicBase() {
    return dynamicBase;
  }

  public void setDynamicBase(String dynamicBase) {
    this.dynamicBase = dynamicBase;
  }

  public String getPageCacheSize() {
    return pageCacheSize;
  }

  public void setPageCacheSize(String pageCacheSize) {
    this.pageCacheSize = pageCacheSize;
  }

  public String getDocCacheSize() {
    return docCacheSize;
  }

  public void setDocCacheSize(String docCacheSize) {
    this.docCacheSize = docCacheSize;
  }

  public String getImgCacheSize() {
    return imgCacheSize;
  }

  public void setImgCacheSize(String imgCacheSize) {
    this.imgCacheSize = imgCacheSize;
  }

  public String getMailServer() {
    return mailServer;
  }

  public void setMailServer(String mailServer) {
    this.mailServer = mailServer;
  }

  public String getMailSender() {
    return mailSender;
  }

  public void setMailSender(String mailSender) {
    this.mailSender = mailSender;
  }

  public String getMailAddress() {
    return mailAddress;
  }

  public void setMailAddress(String mailAddress) {
    this.mailAddress = mailAddress;
  }
}
